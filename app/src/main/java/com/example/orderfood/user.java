package com.example.orderfood;

public class user {
    String name, price, discount, profileimage;

    public user() {
    }

    public String getname() {
        return name;
    }

    public String getprice() {
        return price;
    }

    public String getdiscount() {
        return discount;
    }

    public String getprofileimage() {
        return profileimage;
    }
}
